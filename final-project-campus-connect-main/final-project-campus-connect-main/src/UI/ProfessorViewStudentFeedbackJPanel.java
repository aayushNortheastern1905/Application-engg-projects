    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRow = tblFeedback.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            DefaultTableModel model = (DefaultTableModel) tblFeedback.getModel();
            String studentName = (String) model.getValueAt(selectedRow, 0);
            String studentContact = (String) model.getValueAt(selectedRow, 1);
            String studentEmail = (String) model.getValueAt(selectedRow, 2);
            String studentFeedback = (String) model.getValueAt(selectedRow, 3);
            String isStudentGraduated = (String) model.getValueAt(selectedRow, 4);       

            txtName.setText(studentName);
            txtContact.setText(studentContact);
            txtEmail.setText(studentEmail);
            txtGraduated.setText(isStudentGraduated);
            txtFeedback.setText(studentFeedback);

        }
    }//GEN-LAST:event_btnViewActionPerformed


