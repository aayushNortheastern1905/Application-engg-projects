    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblFeedback.getModel();
        dtm.setRowCount(0);
        for(Student student : studentDirectory.getStudentDirectory()) {
                Object[] row = new Object[5];
                row[0] = student.getStudentName();
                row[1] = student.getStudentPhoneNumber();
                row[2] = student.getStudentEmailID();
                row[3] = student.getFeedback();
                if(student.isDegreegranted() == true) {
                    row[4] = "Yes";
                } else {
                    row[4] = "No";
                }
                dtm.addRow(row);
        }
    }
