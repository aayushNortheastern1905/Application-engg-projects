        private void populateTable(){
            System.out.println(studentDirectory.getStudentDirectory());
        DefaultTableModel dtm = (DefaultTableModel) tblReviewFinancialAid.getModel();
        dtm.setRowCount(0);
        for (Student student : studentDirectory.getStudentDirectory()){
           if(student.isFinancialAidRequested() && !student.isFinancialAidGranted()){
               Object[] row = new Object[6];
                row[0] = student.getStudentName();
                row[1] = student.getStudentUserName();
                row[2] = student.getStudentEmailID();
                row[3] = student.isFinancialAidRequested();
                row[4] = student.getFinancialAidReason();
                row[5] = student.getStudentID();
                dtm.addRow(row);         
            }
        }
    }
