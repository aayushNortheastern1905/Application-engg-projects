    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblReviewFinancialAid.getSelectedRow();

        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details", "Warning",JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) tblReviewFinancialAid.getModel();

            String studentName = (String) model.getValueAt(selectedRow, 0);

            lblNameValue.setText(studentName);

            boolean financialAidRequested = (boolean) model.getValueAt(selectedRow,3);

            if(financialAidRequested){
                rdBtnNo.setSelected(true);
            }
            else{
                rdBtnYes.setSelected(false);
            }
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnGrantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrantActionPerformed
        // TODO add your handling code here:
        if("".equals(lblNameValue.getText())) {
            JOptionPane.showMessageDialog(null,"Please select a student");
        }
        else {
            int selectedRow = tblReviewFinancialAid.getSelectedRow();

            DefaultTableModel model = (DefaultTableModel) tblReviewFinancialAid.getModel();

            String studentId = (String) model.getValueAt(selectedRow, 5);

            Student student = studentDirectory.searchStudentByStudentId(studentId);

            if(rdBtnYes.isSelected()){
                student.setFinancialAidGranted(true);
            }
            else{
                student.setFinancialAidGranted(false);
            }

            JOptionPane.showMessageDialog(null,"Financial Aid Granted!");

            lblNameValue.setText("");
            rdBtnNo.setSelected(true);
            populateTable();
        }
    }//GEN-LAST:event_btnGrantActionPerformed

