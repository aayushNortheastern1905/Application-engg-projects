    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblannounce.getModel();
        dtm.setRowCount(0);
        for(Announcement announcement: announcementDirectory.getAnnouncementDirectory()){

                Object[] row = new Object[4];
            row[0] = announcement.getType();
            row[1] = announcement.getTitle();
            row[2] = announcement.getDescription();
            row[3] = announcement.getBrokerName();
            
            dtm.addRow(row);
    }
    }

