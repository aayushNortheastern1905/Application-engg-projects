    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = tblannounce.getSelectedRow();
    if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details", "Warning",JOptionPane.WARNING_MESSAGE);
        }
    else {
         DefaultTableModel model = (DefaultTableModel) tblannounce.getModel();
            String type = (String) model.getValueAt(selectedRow, 0);
            String  title = (String) model.getValueAt(selectedRow, 1);
            String description = (String) model.getValueAt(selectedRow, 2);
            String brokerName = (String) model.getValueAt(selectedRow, 3);
            
            txttype.setText(type);
            txttitle.setText(title);
            txtdescription.setText(description);
            txtbroker.setText(brokerName);
    }
    }//GEN-LAST:event_jButton2ActionPerformed


