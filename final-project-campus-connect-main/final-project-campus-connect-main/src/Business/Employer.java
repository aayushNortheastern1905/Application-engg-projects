package Business;

/**
 *
 * @author Ayush
 */
public class Employer {
    private String employerID;
    private String employerName;
    private String employerUserName;
    private String employerPassword;
    private String employerPhoneNumber;
    private String employerEmailID;
    private String employerOrganization;

    public String getEmployerID() {
        return employerID;
    }

    public void setEmployerID(String employerID) {
        this.employerID = employerID;
    }

    public String getEmployerName() {
        return employerName;
    }

    public void setEmployerName(String employerName) {
        this.employerName = employerName;
    }

    public String getEmployerUserName() {
        return employerUserName;
    }

    public void setEmployerUserName(String employerUserName) {
        this.employerUserName = employerUserName;
    }

    public String getEmployerPassword() {
        return employerPassword;
    }

    public void setEmployerPassword(String employerPassword) {
        this.employerPassword = employerPassword;
    }

    public String getEmployerPhoneNumber() {
        return employerPhoneNumber;
    }

    public void setEmployerPhoneNumber(String employerPhoneNumber) {
        this.employerPhoneNumber = employerPhoneNumber;
    }

    public String getEmployerEmailID() {
        return employerEmailID;
    }

    public void setEmployerEmailID(String employerEmailID) {
        this.employerEmailID = employerEmailID;
    }

    public String getEmployerOrganization() {
        return employerOrganization;
    }

    public void setEmployerOrganization(String employerOrganization) {
        this.employerOrganization = employerOrganization;
    }
    
    
}
