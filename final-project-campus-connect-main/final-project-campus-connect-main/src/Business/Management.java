public class Management {
    private String managementID;
    private String managementName;
    private String managementEmailID;
    private String managementUserName;
    private String managementPassword;
    private String managementPhoneNumber;

    public String getManagementID() {
        return managementID;
    }

    public void setManagementID(String managementID) {
        this.managementID = managementID;
    }

    public String getManagementName() {
        return managementName;
    }

    public void setManagementName(String managementName) {
        this.managementName = managementName;
    }

    public String getManagementEmailID() {
        return managementEmailID;
    }

    public void setManagementEmailID(String managementEmailID) {
        this.managementEmailID = managementEmailID;
    }

    public String getManagementUserName() {
        return managementUserName;
    }

    public void setManagementUserName(String managementUserName) {
        this.managementUserName = managementUserName;
    }

    public String getManagementPassword() {
        return managementPassword;
    }

    public void setManagementPassword(String managementPassword) {
        this.managementPassword = managementPassword;
    }

    public String getManagementPhoneNumber() {
        return managementPhoneNumber;
    }

    public void setManagementPhoneNumber(String managementPhoneNumber) {
        this.managementPhoneNumber = managementPhoneNumber;
    }
    
}
