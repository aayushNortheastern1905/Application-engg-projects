public class InterestedCustomer {
    private String interestedCustomerID;
    private String interestedCustomerName;
    private String interestedCustomerUserName;
    private String interestedCustomerPhoneNumber;
    private String numberOfRoomates;
    private String depositAmount;
    private String feedback;

    public String getInterestedCustomerID() {
        return interestedCustomerID;
    }

    public void setInterestedCustomerID(String interestedCustomerID) {
        this.interestedCustomerID = interestedCustomerID;
    }

    public String getInterestedCustomerName() {
        return interestedCustomerName;
    }

    public void setInterestedCustomerName(String interestedCustomerName) {
        this.interestedCustomerName = interestedCustomerName;
    }

    public String getInterestedCustomerUserName() {
        return interestedCustomerUserName;
    }

    public void setInterestedCustomerUserName(String interestedCustomerUserName) {
        this.interestedCustomerUserName = interestedCustomerUserName;
    }

    public String getInterestedCustomerPhoneNumber() {
        return interestedCustomerPhoneNumber;
    }

    public void setInterestedCustomerPhoneNumber(String interestedCustomerPhoneNumber) {
        this.interestedCustomerPhoneNumber = interestedCustomerPhoneNumber;
    }

    public String getNumberOfRoomates() {
        return numberOfRoomates;
    }

    public void setNumberOfRoomates(String numberOfRoomates) {
        this.numberOfRoomates = numberOfRoomates;
    }

    public String getDepositAmount() {
        return depositAmount;
    }

    public void setDepositAmount(String depositAmount) {
        this.depositAmount = depositAmount;
    }

    public String getFeedback() {
        return feedback;
    }

    public void setFeedback(String feedback) {
        this.feedback = feedback;
    }
    
    
}
