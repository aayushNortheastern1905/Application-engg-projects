public class CertificateAuthority {
    private String certificateAuthorityID;
    private String certificateAuthorityName;
    private String certificateAuthorityUserName;
    private String certificateAuthorityPassword;
    private String certificateAuthorityPhoneNumber;
    private String certificateAuthorityEmailID;

    public String getCertificateAuthorityID() {
        return certificateAuthorityID;
    }

    public void setCertificateAuthorityID(String certificateAuthorityID) {
        this.certificateAuthorityID = certificateAuthorityID;
    }

    public String getCertificateAuthorityName() {
        return certificateAuthorityName;
    }

    public void setCertificateAuthorityName(String certificateAuthorityName) {
        this.certificateAuthorityName = certificateAuthorityName;
    }

    public String getCertificateAuthorityUserName() {
        return certificateAuthorityUserName;
    }

    public void setCertificateAuthorityUserName(String certificateAuthorityUserName) {
        this.certificateAuthorityUserName = certificateAuthorityUserName;
    }

    public String getCertificateAuthorityPassword() {
        return certificateAuthorityPassword;
    }

    public void setCertificateAuthorityPassword(String certificateAuthorityPassword) {
        this.certificateAuthorityPassword = certificateAuthorityPassword;
    }

    public String getCertificateAuthorityPhoneNumber() {
        return certificateAuthorityPhoneNumber;
    }

    public void setCertificateAuthorityPhoneNumber(String certificateAuthorityPhoneNumber) {
        this.certificateAuthorityPhoneNumber = certificateAuthorityPhoneNumber;
    }

    public String getCertificateAuthorityEmailID() {
        return certificateAuthorityEmailID;
    }

    public void setCertificateAuthorityEmailID(String certificateAuthorityEmailID) {
        this.certificateAuthorityEmailID = certificateAuthorityEmailID;
    }

    
}
